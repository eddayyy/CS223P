def addition(*, left = 0 , right = 0):
    return left + right

def subtraction(*, left = 0 , right = 0):
    return left - right

def multiplication(*, left = 0 , right = 0):
    return left * right

def division(*, left = 0 , right = 0):
    return left / right
    